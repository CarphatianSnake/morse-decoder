const MORSE_TABLE = {
  '.-':     'a',
  '-...':   'b',
  '-.-.':   'c',
  '-..':    'd',
  '.':      'e',
  '..-.':   'f',
  '--.':    'g',
  '....':   'h',
  '..':     'i',
  '.---':   'j',
  '-.-':    'k',
  '.-..':   'l',
  '--':     'm',
  '-.':     'n',
  '---':    'o',
  '.--.':   'p',
  '--.-':   'q',
  '.-.':    'r',
  '...':    's',
  '-':      't',
  '..-':    'u',
  '...-':   'v',
  '.--':    'w',
  '-..-':   'x',
  '-.--':   'y',
  '--..':   'z',
  '.----':  '1',
  '..---':  '2',
  '...--':  '3',
  '....-':  '4',
  '.....':  '5',
  '-....':  '6',
  '--...':  '7',
  '---..':  '8',
  '----.':  '9',
  '-----':  '0',
};

function decode(expr) {
  let res = [];

  for (let i = 0; i < expr.length; i += 10) {
    
    const letterCode = expr.slice(i, i + 10);

    if (letterCode === '**********') {
      res.push(' ');
    } else {
      let letter = '';
      for (let j = 0; j < 10; j += 2) {
        if (letterCode.slice(j, j + 2) === '10') {
          letter += '.';
        }
        if (letterCode.slice(j, j + 2) === '11') {
          letter += '-';
        }
      }
      res.push(MORSE_TABLE[letter]);
    }
  }
  console.log(res.join(''));
  return res.join('');
}

